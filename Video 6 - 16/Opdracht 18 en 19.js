let numberOne = 12;
let numberTwo = 1;

console.log(numberOne + numberTwo);

numberOne = ++numberOne;
console.log(numberOne);

numberOne = 12;
numberOne += numberTwo;
console.log(numberOne);

numberOne = 12;
console.log(numberOne - numberTwo);

numberOne = --numberOne;
console.log(numberOne);

numberOne = 12;
numberOne -= numberTwo;
console.log(numberOne);
